% Dados iniciais
e = [0.02 0.05 0.1 0.15 0.20 0.25];
s = [40 37.5 43 52 60 55];
I = zeros(1,3);

% ------------------------------------------------------------------------------

% Regra do Trapézio
% [0,02 ~ 0,05]
I(1) = (e(2)-e(1))*(s(1)+s(2))/2;

% [0,05 ~ 0,1]
I(2) = (e(3)-e(2))*(s(2)+s(3))/2;

% 3/8 de Simpson
% [0,1 ~ 0,25]
I(3) = ((e(6)-e(3))/8)*(s(3)+(3*s(4))+(3*s(5))+s(6));

printf("O valor do módulo de rigidez é aproximadamente: %f\n", sum(I));

